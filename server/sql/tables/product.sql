CREATE TABLE product (
	id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY, 
    categoryId INTEGER REFERENCES category(id),
	name VARCHAR(100),
    price MONEY,
    createdON TIMESTAMP NOT NULL,
    modifiedON TIMESTAMP NULL,
    isActive BOOLEAN DEFAULT TRUE
);

